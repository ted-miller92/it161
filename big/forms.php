                <h2>Forms</h2>
                <p>Any business or service will eventually need to gather information from clients or customers. HTML forms offer a variety of input fields for users to enter information and send it to the server. As developers and designers, we should be doing the heavy lifting here. Any time the client or customer cannot figure out how to interact with our website, we are pushing them away from us and towards our competitors. How do we not aggravate users?</p>
                <p>Basically: Make it easy. This is the overarching theme when it comes to how to design your forms. Alex Birkett (1) goes over a number of different methods and insights concerning making your forms easier for the end user. Simple things like omitting unnecessary fields or options, aligning all elements of the form in one column, and auto-formatting phone numbers and email addresses make the user experience easier and more streamlined.</p>
                <p>Birkett also covered a few other things I thought were rather insightful. The order in which the elements of your form appear makes a difference to the user. Using a retail site as an example, you may not want to start your form by asking for the user’s credit card information. Instead, Start with the name and shipping information. Evidence also shows that one of the top reasons for “abandoned carts” is that users leave the web page to look for coupons, so making the coupon field less prominent ensures the user turns into a customer.</p>
                <p>Another thing to consider is accessibility. Make it easy for everyone. WebAIM provides some guidelines(2) for making the forms on your web pages accessible. This makes the input elements in the form navigable with assistive devices, as well as all the standard usability guidelines.</p>
