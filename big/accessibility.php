                <h2>Accessibility</h2>
                    <p>HTML, CSS and Javascript are often the first language any new developer learns. These days, there are numerous resources online for learning how to author web pages. All you need is a computer with an internet connection, and the gumption to learn a new skill.</p>
                    <p>Unfortunately, these languages do not natively include features that allow for wide accessibility, particularly for people who depend on assistive technologies or who rely on keyboard navigation (1). According to Web Accessibility in Mind, around one in four Americans have some type of disability (2). While this doesn’t imply that this entire population depends on assistive technology, there are still millions of people with limited or hindered access to the content on the web. Considering all the great things on the internet, this is a real shame!</p>
                    <img src=../images/grumpycat.png alt="A grumpy looking cat" style="width:400px; margin-left:15px;">
                    <p>While it is a shame that so many cannot access all the cat videos and trivial knowledge we have at our fingertips these days, there are other more pressing issues that people with disabilities face. A 2018 survey found that 41% of government websites did not meet current accessibility standards (3). This includes online resources for paying taxes, obtaining licenses and permits, and retrieving election information.</p>
                    <p>Wouldn’t you think that resources that serve crucial roles in our society and in our personal lives would be a little easier to access? Speaking from my own experience, I absolutely abhor trying to get anything done on a government website, and I don’t even depend on assistive technology.</p>
                    <p>Web Accessibility should be one of the fundamental concepts and principles that designers and developers utilize in web authoring. In the case of JavaScript or PHP or other scripting languages, programs will simply not work if the code is written incorrectly. This should be the case when structuring web pages. We must design and develop with purpose and intent. It will make us all better, more thoughtful developers, and it will avail content for anyone who wishes or requires access to it.</p>
