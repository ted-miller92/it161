                <h2>Social Media Optimization</h2>
                <p>We all spend more time than we’d like scrolling through our feeds. Whether it’s Twitter, Instagram or Reddit (Do people still use Facebook for anything other than fomenting failed revolutions?), this is a reality we may as well admit to, and we may as well embrace it too.</p>
                <p>In 2018, Instagram reached 1 billion users (1). If you or your business also have an Instagram account, think of this as nine hundred ninety-nine million, nine hundred ninety nine thousand, nine hundred and ninety nine potential clients.</p>
                <p>Simply having a presence on social media will make a difference. If you own a restaurant, you can share specials or menu updates. People will see these posts as they scroll through their feed, but they probably won’t be checking your website online every day. Your future clients and customers are not actively waiting for these updates, you must bring news to them.</p>
                <p>Fortunately, social media platforms make this very easy. You just need to follow a few guidelines and have a base knowledge of how the platform works. On both Instagram and Twitter, adding hashtags to posts allows you to associate your content with similar content. Append a “#SeattleEats” to every post you make, and your content will work it’s way into the feeds of thousands of hungry Seattleites.</p>
                <p>It is also worth noting the value of engaging with people. There is value in knowing the business you support consists of regular people, with similar interests and lifestyles (2). Use colloquialisms. Don’t be stale, cold or distant. Be warm and funny. People appreciate jokes. Wendy’s has been a great example of a successful social media campaign.</p>
